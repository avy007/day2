public class whilenatrualsum {

	public static void main(String[] args) {
//Write a Program for the sum of n natural numbers eg. Input N=5 output 1+2+3+4+5		

		int n = 1;
		int sum=0;
		while(n<=5) {
			
			
			System.out.println(n);//5
			sum=sum+n; //15
			n++;
			
			
			
		}
		System.out.println(sum);
		
	}

}
